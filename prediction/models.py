from django.db import models

# Create your models here.
class Home_Details(models.Model):
    img=models.ImageField(upload_to="pics1")
    bedroom=models.CharField(max_length=50)
    bathroom=models.CharField(max_length=50)
    area=models.CharField(max_length=80)
    city=models.CharField(max_length=80)
    house_img1=models.ImageField(upload_to="pics1")
    house_img2=models.ImageField(upload_to="pics1")
    house_img3=models.ImageField(upload_to="pics1")
    price=models.IntegerField()
    sqft=models.CharField(max_length=15)
    desc=models.TextField()
    available=models.CharField(max_length=30)
    time=models.CharField(max_length=20)
    parking=models.CharField(max_length=20)
    outdoor=models.CharField(max_length=20)
    ac=models.CharField(max_length=20)
    hoa=models.CharField(max_length=20)
    pricepersqft=models.CharField(max_length=20)
    listed=models.CharField(max_length=20)
    year_built=models.CharField(max_length=10)
    property_type=models.CharField(max_length=30)
    property_subtype=models.CharField(max_length=30)
    building1=models.CharField(max_length=30)
    building2=models.CharField(max_length=30)
    condition=models.CharField(max_length=30)
    parcel_number=models.CharField(max_length=18)